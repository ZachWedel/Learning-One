course = "  Python Programming"
print(course)
print(course.upper())  # Convert to uppercase
print(course.lower())  # Convert to lowercase
print(course.title())  # Convert to title case
print(course.strip())  # Remove leading and trailing whitespace
print(course.lstrip())  # Remove leading whitespace
print(course.rstrip())  # Remove trailing whitespace
print(course.find("Pro"))  # Find substring
print(course.replace("p", "j"))  # Replace substring
print("pro" in course)  # Check substring presence
print("swift" not in course)  # Check substring absence
